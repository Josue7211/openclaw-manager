/**
 * Theme Definitions — 24 built-in theme presets + type system for the theming engine.
 *
 * Each ThemeDefinition is a JSON-serializable object mapping CSS custom property names
 * (without the `--` prefix) to values. The theme engine applies these by iterating
 * the `colors` record and calling `el.style.setProperty('--' + key, value)`.
 *
 * Two-tier architecture:
 *   Tier 1 — Surfaces, text, borders, glass, hover, overlays (fixed per base theme)
 *   Tier 2 — Accent colors, glow, status colors (user-overridable per theme)
 */

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface ThemeDefinition {
  id: string
  name: string
  category: 'dark' | 'light' | 'high-contrast' | 'colorful'
  builtIn: boolean
  artwork?: string
  colors: Record<string, string>
  fonts?: { body?: string; heading?: string; mono?: string; ui?: string }
  fontScale?: number
}

export interface UserThemeOverrides {
  themeId: string
  accent?: string
  secondary?: string
  tertiary?: string
  glow?: string
  logo?: string
  fonts?: { body?: string; heading?: string; mono?: string; ui?: string }
  fontScale?: number
  pinned?: boolean
  glowOpacity?: number    // 0 to 0.25 (controls --glow-opacity)
  borderRadius?: number   // 0 to 24 (controls --radius-md in px)
  panelOpacity?: number   // 0.4 to 1.0 (controls glass panel alpha)
}

export interface ThemeSchedule {
  type: 'sunrise-sunset' | 'manual' | 'none'
  sunriseSunset?: { dayThemeId: string; nightThemeId: string }
  manual?: Array<{
    startHour: number
    startMinute: number
    endHour: number
    endMinute: number
    themeId: string
  }>
}

export interface ThemeState {
  mode: 'dark' | 'light' | 'system'
  activeThemeId: string
  overrides: Record<string, UserThemeOverrides>
  customThemes: ThemeDefinition[]
  schedule?: ThemeSchedule
  pageOverrides?: Record<string, string>
  categoryOverrides?: Record<string, string>
  globalFontOverride?: boolean
  /** When true, System mode uses GTK theme mapping (wallbash/counterparts).
   *  When false, System mode just picks built-in dark/light based on OS preference. */
  useGtkTheme?: boolean
  lastModified?: number
}

